package p2023_7_19;

public class Practice02 {

	public static void main(String[] args) {

		int x = 0;
		System.out.print("x = 0일 때, 참인 것은" + "\n");
		if (x == 0)
			System.out.println("x == 0");
		if (x != 0)
			System.out.println("x != 0");
		if (!(x == 0))
			System.out.println("!(x == 0)");
		if (!(x != 0))
			System.out.println("!(x != 0)");

		int x1 = 1;
		System.out.print("x = 0일 때, 참인 것은" + "\n");
		if (x1 == 0)
			System.out.println("x == 0");
		if (x1 != 0)
			System.out.println("x != 0");
		if (!(x1 == 0))
			System.out.println("!(x == 0)");
		if (!(x1 != 0))
			System.out.println("!(x != 0)");

	}

}
