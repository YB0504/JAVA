package p2023_7_16;

public class Operater_03 {

	public static void main(String[] args) {
		
//		혼공자 교재 산술연산자 예제
		int pencils = 534;
		int students = 30;
		
		int pencilsPerStudents = (pencils / students);
		System.out.println(pencilsPerStudents);
		
		int pencilsLeft = (pencils % students);
		System.out.println(pencilsLeft);
		
	}

}
