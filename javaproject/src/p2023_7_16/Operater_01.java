package p2023_7_16;

public class Operater_01 {

	public static void main(String[] args) {
		
//		 대입 연산자 복습
//		num += 2; 는 선언된 변수의 숫자에 2를 더하고 num에 대입하라는 뜻
		
		int num = 10;
		num += 2;
		System.out.println(num);
		
		num -= 2;
		System.out.println(num);
		
		num /= 2;
		System.out.println(num);
		
		num *= 2;
		System.out.println(num);
		
		num %= 2;
		System.out.println(num);
		
		
		
		
	}

}
