package day01;

public class Test1 {

	public static void main(String[] args) {
		
//		변수 선언
//		변수는 메모리 상의 데이터를 저장하기 위한 기억 공간의 이름
//		변수를 만드는 형식 : 자료형 변수명 = 데이터값;
		
//		지역 변수 : 메인 메소드 안에서만 사용 가능하다.
//		변수의 자료형과 데이터 값이 일치해야 한다.
		// 정수 값
		int age = 25;
		System.out.println("나이 : " + age);
		
		// 실수 값
		double l = 1.5;
		double r = 2.0;
		System.out.println("왼쪽 시력 : " + l);
		System.out.println("오른쪽 시력 : " + r);
		
		// 한 글자
		char c = '자';
		System.out.println(c);
		
		// true or false
		boolean b = true;
		System.out.println(b);
		
		
		
		
	}

}
